const btData = {
    namespaced: true,
    state: () => ({
        btData: {}, // 存储频谱数据
    }),
    mutations: {
      setbtData(state, payload) {
        state.btData = payload;
      },
    },
    actions: {
      updatebtData({ commit }, data) {
        commit("setbtData", data);
      },
    },
  };
  
  export default btData;
  